#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		n;
	t_list	*tmp;

	n = 0;
	tmp = lst;
	while (tmp)
	{
		tmp = tmp->next;
		n++;
	}	
	return (n);
}
